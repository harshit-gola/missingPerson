						    <p>When someone you care about goes missing, it can be a very frightening, lonely and overwhelming time.</p>
							<p>It is quite rare for someone to just disappear but when it does happen and we don’t know where someone is, it can leave us feeling very confused and things may not make sense to us anymore.</p>
							<p>It is important that you don’t feel alone with these feelings and we hope that we are able to help you understand what is happening a little more.</p>

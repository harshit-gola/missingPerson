					    <h3>What does missing mean?</h3>
					    <p>
					    	When you hear people talking about someone going missing this is what the police mean:
							The Association of Chief Police Officers (ACPO) defines a missing person as:
						</p>
						
						<em>
							anyone whose whereabouts is unknown whatever the circumstances of disappearance. They will be considered missing until located and their well-being or otherwise established”. (ACPO, 2005: 8).
						</em>

						<p>
							That really means that if the Police don’t know where someone is then they will be known as missing until they know for certain what has happened.
						</p>


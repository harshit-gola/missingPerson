					    <h3>I feel really scared and confused, is that normal?</h3>
							<p>	We know from speaking with people that when someone you love or care about goes missing, you can be left feeling a rollercoaster of emotions. Sometimes you might be sad and tearful, other times you might feel angry at the person for not being there anymore.</p>

							<p>Whatever you feel is ok, no one person reacts in the same way and it is ok to be angry, sad, scared, confused, these are all normal reactions to the shock of someone not being here.</p>

							<p>You might feel that you don’t want to talk to anyone about how you are feeling, that’s ok, but sometimes letting others know that you are having a hard time can help you to not feel as alone. You might find that other people in your family or your friends are also feeling similar to you.</p>

							<p>If you do feel like you want to talk to someone about how you are feeling, you could try a friend or a teacher at school or college or you can contact us by text, email or phone and we will listen and be there for you.</p>
					  </div>
					  <div id="menu3" class="tab-pane fade">
					    <h3>Why is nobody telling me what's going on?</h3>
						    <p>We know that some young people feel left out when someone goes missing. That might be because your parents, or other family feel it is better not to worry you or upset you.</p>
							<p>They may not realise that you already know something is wrong, and that it might feel worse not knowing, as you can then imagine all sorts of horrible things have happened.</p>
							<p>Let people know that you want to be included and that you would rather know, even though there aren’t answers, at least you can all share that together.</p>
					  </div>
					  <div id="menu4" class="tab-pane fade">
					    <h3>Who looks for people when they go missing?</h3>
						    <p>When someone goes missing and people are worried about them. Usually a family member will speak with the police and let them know what has happened. The police will then begin searches to find out what has happened to the missing person, to try and make sure that they are safe.</p>
							<p>This might include searching with dogs or helicopters, checking someone’s mobile phone or bank account to see if and when they have used them. All of these things can help the police trace the missing person to ask if they are ok.</p>
							<p>The police and our charity may also send out posters with a photo of your missing loved one on it, and sometimes we put this on Facebook and Twitter as well. Doing this means lots of people will see the poster and hopefully let us know they have seen your missing person.</p>
					  </div>
					  <div id="menu5" class="tab-pane fade">
					    <h3>What if the person is missing for a long time?</h3>
						    <p>WMost people who go missing do come back or are found pretty quickly. But, we know that sometimes people aren’t and that families sometimes are left waiting for news.</p>
							<p>It can be very difficult to keep doing normal things when this happens, sometimes you might feel that life will never be the same again. You might find that as time goes by people don’t talk as much about your person, or maybe people don’t know what to say, which can then make it hard for you to talk about how you feel.</p>
							<p>It can be helpful to write down how you feel, even if you never show it to anyone. Or send us a text or email telling us how it feels inside. You don’t have to be alone.</p>
							<p>We know that people who have someone missing can find it hard to know how to explain to other people what is happening to them. That’s ok, it’s normal to feel that way when we are dealing with difficult things in our lives.</p>
							<p>Perhaps you can start by letting people around you know that, tell them that it doesn’t make sense to you, and how that has left you feeling confused, alone and scared.</p>
					  </div>
					  <div id="menu6" class="tab-pane fade">
					    <h3>Remembering your missing person</h3>
						    <p>Just because someone isn’t here with us, it is important to think about them and remember happy times and stories. Sometimes not everyone in your family or friends will feel the same way about doing this, so it can be good to find your own ways to remember.</p>

							<p>Maybe write letters to your missing person, keep them safe in a box or drawer. Writing poems or songs can be a good way to think about how you feel when you might not want to talk to someone about them.</p>

							<p>Looking at photographs of special times can also help. Keep them somewhere safe so you can look at them when you feel you would like to spend some time thinking about your missing person and remembering what they mean to you.</p>
					  </div>
					  <div id="menu7" class="tab-pane fade">
					    <h3>Looking after yourself</h3>
						    <p>Remember none of this is your fault – sometimes you might find yourself thinking if only, or what if,  or even feeling bad that something you did or said made them leave.  These are all normal ways to feel after you lose someone.</p>

							<p>When we try and make sense of difficult situations, we often have to ask ourselves lots of questions – even when we know there aren’t any answers.</p>

							<p>Try and find things that help you to relax, and help you to not always think about your person who is missing. Still do fun things like reading or running, whatever makes you feel good. Do it as it is important you look after yourself.</p>
